
public class Course 
{
	double id;
	String courseName;
	String lecturerName;
	double percentage;
	
	public Course(double id, String courseName, String lecturerName, double percentage)
	{
		this.id = id;
		this.courseName = courseName;
		this.lecturerName = lecturerName;
		this.percentage = percentage;
	}
	
}
